package rp3.marketforce.models.pedido;

import android.database.Cursor;

import java.util.ArrayList;
import java.util.List;

import rp3.data.entity.EntityBase;
import rp3.db.QueryDir;
import rp3.db.sqlite.DataBase;
import rp3.marketforce.db.Contract;
import rp3.util.CursorUtils;

/**
 * Created by magno_000 on 22/04/2016.
 */
public class MarcaTarjeta extends EntityBase<MarcaTarjeta> {

    private long id;
    private int idMarcaTarjeta;
    private String descripcion;

    @Override
    public long getID() {
        return id;
    }

    @Override
    public void setID(long id) {
        this.id = id;
    }

    @Override
    public boolean isAutoGeneratedId() {
        return true;
    }

    @Override
    public String getTableName() {
        return Contract.MarcaTarjeta.TABLE_NAME;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public int getIdMarcaTarjeta() {
        return idMarcaTarjeta;
    }

    public void setIdMarcaTarjeta(int idMarcaTarjeta) {
        this.idMarcaTarjeta = idMarcaTarjeta;
    }

    @Override
    public void setValues() {
        setValue(Contract.MarcaTarjeta.COLUMN_ID_MARCA_TARJETA, this.idMarcaTarjeta);
        setValue(Contract.MarcaTarjeta.COLUMN_DESCRIPCION, this.descripcion);
    }

    @Override
    public Object getValue(String key) {
        return idMarcaTarjeta;
    }

    @Override
    public String getDescription() {
        return descripcion;
    }

    public static List<MarcaTarjeta> getMarcasTarjetas(DataBase db) {
        Cursor c = db.query(Contract.MarcaTarjeta.TABLE_NAME, new String[] {Contract.MarcaTarjeta._ID, Contract.MarcaTarjeta.COLUMN_ID_MARCA_TARJETA, Contract.MarcaTarjeta.COLUMN_DESCRIPCION});

        List<MarcaTarjeta> list = new ArrayList<MarcaTarjeta>();
        while(c.moveToNext()){
            MarcaTarjeta marcaTarjeta = new MarcaTarjeta();
            marcaTarjeta.setID(CursorUtils.getInt(c, Contract.MarcaTarjeta._ID));
            marcaTarjeta.setIdMarcaTarjeta(CursorUtils.getInt(c, Contract.MarcaTarjeta.COLUMN_ID_MARCA_TARJETA));
            marcaTarjeta.setDescripcion(CursorUtils.getString(c, Contract.MarcaTarjeta.COLUMN_DESCRIPCION));
            list.add(marcaTarjeta);
        }
        c.close();
        return list;
    }

    public static List<MarcaTarjeta> getMarcasTarjetasPorBanco(DataBase db, int idBanco) {
        String query = QueryDir.getQuery(Contract.MarcaTarjeta.QUERY_TARJETAS_POR_BANCO);
        Cursor c = db.rawQuery(query, new String[]{idBanco + ""});

        List<MarcaTarjeta> list = new ArrayList<MarcaTarjeta>();
        while(c.moveToNext()){
            MarcaTarjeta marcaTarjeta = new MarcaTarjeta();
            marcaTarjeta.setID(CursorUtils.getInt(c, Contract.MarcaTarjeta._ID));
            marcaTarjeta.setIdMarcaTarjeta(CursorUtils.getInt(c, Contract.MarcaTarjeta.COLUMN_ID_MARCA_TARJETA));
            marcaTarjeta.setDescripcion(CursorUtils.getString(c, Contract.MarcaTarjeta.COLUMN_DESCRIPCION));
            list.add(marcaTarjeta);
        }
        c.close();
        return list;
    }
}