package rp3.marketforce.models.pedido;

import android.database.Cursor;

import java.util.ArrayList;
import java.util.Date;
import java.util.List;

import rp3.data.entity.EntityBase;
import rp3.db.sqlite.DataBase;
import rp3.marketforce.db.Contract;
import rp3.util.CursorUtils;

/**
 * Created by magno_000 on 02/05/2017.
 */

public class VentaPerdida extends EntityBase<VentaPerdida> {

    private long id;
    private String codigoProducto;
    private Date fecha;
    private boolean pendiente;

    @Override
    public long getID() {
        return id;
    }

    @Override
    public void setID(long id) {
        this.id = id;
    }

    @Override
    public boolean isAutoGeneratedId() {
        return true;
    }

    @Override
    public String getTableName() {
        return Contract.VentaPerdida.TABLE_NAME;
    }

    @Override
    public void setValues() {
        setValue(Contract.VentaPerdida.COLUMN_CODIGO_PRODUCTO, this.codigoProducto);
        setValue(Contract.VentaPerdida.COLUMN_FECHA, this.fecha);
        setValue(Contract.VentaPerdida.COLUMN_PENDIENTE, this.pendiente);
    }

    @Override
    public Object getValue(String key) {
        return codigoProducto;
    }

    @Override
    public String getDescription() {
        return codigoProducto;
    }

    public String getCodigoProducto() {
        return codigoProducto;
    }

    public void setCodigoProducto(String codigoProducto) {
        this.codigoProducto = codigoProducto;
    }

    public Date getFecha() {
        return fecha;
    }

    public void setFecha(Date fecha) {
        this.fecha = fecha;
    }

    public boolean isPendiente() {
        return pendiente;
    }

    public void setPendiente(boolean pendiente) {
        this.pendiente = pendiente;
    }

    public static List<VentaPerdida> getPendientes(DataBase db) {
        Cursor c = db.query(Contract.VentaPerdida.TABLE_NAME, new String[] {Contract.VentaPerdida._ID, Contract.VentaPerdida.COLUMN_CODIGO_PRODUCTO, Contract.VentaPerdida.COLUMN_FECHA,
                        Contract.VentaPerdida.COLUMN_PENDIENTE}
                , Contract.VentaPerdida.COLUMN_PENDIENTE + " = ? ", 1);

        List<VentaPerdida> lista = new ArrayList<VentaPerdida>();
        while(c.moveToNext()){
            VentaPerdida venta = new VentaPerdida();
            venta.setID(CursorUtils.getLong(c, Contract.VentaPerdida._ID));
            venta.setCodigoProducto(CursorUtils.getString(c, Contract.VentaPerdida.COLUMN_CODIGO_PRODUCTO));
            venta.setFecha(CursorUtils.getDate(c, Contract.VentaPerdida.COLUMN_FECHA));
            venta.setPendiente(CursorUtils.getBoolean(c, Contract.VentaPerdida.COLUMN_PENDIENTE));
            lista.add(venta);
        }
        c.close();
        return lista;
    }
}
