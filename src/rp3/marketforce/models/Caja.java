    public static Caja getCajaActiva(DataBase db){

        Cursor c = db.query(Contract.Caja.TABLE_NAME, new String[] {Contract.Caja._ID, Contract.Caja.COLUMN_ID_CAJA_CONTROL, Contract.Caja.COLUMN_ACTIVO, Contract.Caja.COLUMN_FECHA_APERTURA,
                Contract.Caja.COLUMN_FECHA_CIERRE, Contract.Caja.COLUMN_MAXIMO_DIAS_APERTURA, Contract.Caja.COLUMN_MONTO_APERTURA, Contract.Caja.COLUMN_MONTO_CIERRE,
                Contract.Caja.COLUMN_NOMBRE, Contract.Caja.COLUMN_SECUENCIA_RECIBO}, Contract.Caja.COLUMN_ACTIVO + " = ? ", 1);

        Caja caja = new Caja();

        if(c.moveToFirst())
        {
            caja.setId(CursorUtils.getInt(c, Contract.Caja._ID));
            caja.setNombre(CursorUtils.getString(c,Contract.Caja.COLUMN_NOMBRE));
            caja.setActivo(CursorUtils.getBoolean(c,Contract.Caja.COLUMN_ACTIVO));
            caja.setFechaApertura(CursorUtils.getDate(c,Contract.Caja.COLUMN_FECHA_APERTURA));
            caja.setFechaCierre(CursorUtils.getDate(c,Contract.Caja.COLUMN_FECHA_CIERRE));
            caja.setIdCajaControl(CursorUtils.getInt(c,Contract.Caja.COLUMN_ID_CAJA_CONTROL));
            caja.setMaximoDiasApertura(CursorUtils.getInt(c,Contract.Caja.COLUMN_MAXIMO_DIAS_APERTURA));
            caja.setSecuenciaRecibo(CursorUtils.getInt(c,Contract.Caja.COLUMN_SECUENCIA_RECIBO));
            caja.setMontoApertura(CursorUtils.getDouble(c,Contract.Caja.COLUMN_MONTO_APERTURA));
            caja.setMontoCierre(CursorUtils.getDouble(c,Contract.Caja.COLUMN_MONTO_CIERRE));
        }

        return caja;
    }
